    private void btnAddFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddFileActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setMultiSelectionEnabled(true);
        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            for (File file : fileChooser.getSelectedFiles()) {
                if (!selectedFiles.contains(file)) {
                    selectedFiles.add(file);
                    comboBoxFile.addItem(file.getName());
                }
            }
        } else if (result == JFileChooser.CANCEL_OPTION) {
            System.out.println("File selection cancelled.");
        }

    }//GEN-LAST:event_btnAddFileActionPerformed

    private void displaySelectedFileContents() {
        int selectedIndex = comboBoxFile.getSelectedIndex();
        String selectedFix = comboFix.getSelectedItem().toString();

        if (selectedIndex != -1 && selectedFix != null && !"".equals(selectedFix)) {
            File selectedFile = selectedFiles.get(selectedIndex);
            if (selectedFile != null) {
                txtareaBST.setText("");
                String content = "";

                HashSet<String> ignoreWords = readIgnoreList();

                BST bst = new BST();
                DocumentFile docFile = new DocumentFile(selectedFile.getPath());
                docFile.processTextDocument(bst, ignoreWords);

                MyLinkedList<String> results = bst.traverse(selectedFix, ignoreWords);
                content = results.join("\n");

                txtareaBST.append(selectedFile.getName() + " " + selectedFix + ":\n" + content + "\n\n");
            }
        } else {
            txtareaBST.setText("Please select an order preference and ensure a file is selected.");
        }
        txtareaBST.setCaretPosition(0);
    }

    private HashSet<String> readIgnoreList() {
        HashSet<String> ignoreWords = new HashSet<>();
        String ignoreText = txtareaIgnore.getText();
        if (!ignoreText.isEmpty()) {
            for (String word : ignoreText.split("\\s+")) {
                ignoreWords.add(word.trim());
            }
        }
        return ignoreWords;
    }

