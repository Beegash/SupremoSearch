    private void btnIgnoreListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgnoreListActionPerformed

        JFileChooser fileChooser = new JFileChooser();
        int result = fileChooser.showOpenDialog(this);

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile2 = fileChooser.getSelectedFile();

            ArrayList<String> ignoreList = readIgnoreList(selectedFile2);

            for (String item : ignoreList) {
                txtareaIgnore.append(item + "\n");
            }
        } else if (result == JFileChooser.CANCEL_OPTION) {
            System.out.println("File selection cancelled.");
        }
        if (checkBoxAll.isSelected()) {
            checkBoxAll.setSelected(false);
        }
        displaySelectedFileContents();
    }//GEN-LAST:event_btnIgnoreListActionPerformed

